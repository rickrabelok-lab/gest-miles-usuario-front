do $$
begin
  if not exists (
    select 1 from pg_policies
    where schemaname = 'public' and tablename = 'perfis' and policyname = 'perfis_select_public'
  ) then
    create policy perfis_select_public on public.perfis for select using (true);
  end if;

  if not exists (
    select 1 from pg_policies
    where schemaname = 'public' and tablename = 'perfis' and policyname = 'perfis_insert_own'
  ) then
    create policy perfis_insert_own on public.perfis for insert with check (auth.uid() = usuario_id);
  end if;

  if not exists (
    select 1 from pg_policies
    where schemaname = 'public' and tablename = 'perfis' and policyname = 'perfis_update_own_or_admin'
  ) then
    create policy perfis_update_own_or_admin on public.perfis
      for update
      using (auth.uid() = usuario_id or public.is_admin())
      with check (auth.uid() = usuario_id or public.is_admin());
  end if;
end $$;

